export default function Community() {
  const [visibleItems, setVisibleItems] = useState<Set<number>>(new Set())
  const itemRefs = useRef<(HTMLDivElement | null)[]>([])

  useEffect(() => {
    const observers = itemRefs.current.map((ref, index) => {
      if (!ref) return null

      const observer = new IntersectionObserver(
        ([entry]) => {
          if (entry.isIntersecting) {
            setTimeout(() => {
              setVisibleItems((prev) => new Set([...prev, index]))
            }, index * 120)
          }
        },
        { threshold: 0.1, rootMargin: '0px 0px -50px 0px' }
      )

      observer.observe(ref)
      return observer
    })

    return () => {
      observers.forEach((observer) => observer?.disconnect())
    }
  }, [])

  return (
    <section id="community" className="py-16 sm:py-24 lg:py-32 bg-black relative overflow-hidden">
      <div className="container mx-auto px-4 sm:px-6">
        <div className="text-center mb-12 sm:mb-16 scroll-animate">
          <h2 className="text-3xl sm:text-4xl md:text-5xl font-bold mb-3 sm:mb-4 px-4">
            <span className="text-white">Join Our</span>{' '}
            <span className="text-ai-green">Community</span>
          </h2>
          <p className="text-base sm:text-lg md:text-xl text-gray-400 max-w-2xl mx-auto px-4">
            Connect with thousands of developers, share experiences, and get help when you need it
          </p>
        </div>

        <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-6 sm:gap-8 max-w-6xl mx-auto">
          {communityLinks.map((link, index) => (
            <div
              key={index}
              ref={(el) => { itemRefs.current[index] = el; }}
              className={`group p-6 sm:p-8 bg-gray-900/50 backdrop-blur-sm rounded-xl border border-gray-800 hover:border-ai-green/50 transition-all duration-500 hover:shadow-lg hover:shadow-ai-green/10 hover:-translate-y-2 cursor-pointer ${
                visibleItems.has(index) ? 'animate-fade-in-up opacity-100' : 'opacity-0 translate-y-10'
              }`}
              style={{ transitionDelay: `${index * 80}ms` }}
            >
              <div className="text-ai-green mb-4 group-hover:scale-110 group-hover:rotate-3 transition-all duration-300">
                {link.icon}
              </div>
              <h3 className="text-xl sm:text-2xl font-semibold mb-2 text-white group-hover:text-ai-green transition-colors duration-300">{link.title}</h3>
              <p className="text-sm sm:text-base text-gray-400 mb-4 leading-relaxed">{link.description}</p>
              <p className="text-ai-green text-xs sm:text-sm font-medium">{link.members}</p>
            </div>
          ))}
        </div>
      </div>
    </section>
  )
}

